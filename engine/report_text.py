"""
Слой текста отчёта. Модуль 12, повествование.

Превращает структуру данных отчёта в аналитическую прозу академического
строя. Разделы следуют разведывательному стандарту, исходная обстановка,
результаты моделирования, пределы достоверности, но поданы строгим научным
тоном, без разговорных ярлыков. Текст собирается детерминированно из фактов
прогона, потому отчёт воспроизводим и при повторной сборке тождествен себе.

Разделение ответственности. Модуль не считает и не извлекает данные, только
облекает готовую структуру в слова. Числа берёт из build_report_data как
есть. Стиль выдержан без двоеточий и длинных тире, нарративной прозой,
оценочные суждения прямые.
"""

from __future__ import annotations


def _cite(phrase: str, key: str) -> str:
    """Оборачивает фразу прозы якорем источника. Слой представления превратит
    якорь в инлайн-ссылку, потому текстовый слой остаётся свободен от HTML.
    Маркер [[ключ||фраза]] в естественной прозе не встречается, потому безопасен
    при экранировании и парсинге. Селективность за вызывающим, якорь ставится
    лишь там, где утверждение прямо опирается на задокументированный факт."""
    return f"[[{key}||{phrase}]]"


_LEVEL_VERB = {
    "red": "ведёт регион в зону срыва",
    "amber": "удерживает регион в устойчивой конфронтации без открытого срыва",
    "green": "оставляет региональный баланс уравновешенным",
}

_AXIS_NATURE = {
    "material": "Движущая сила сценария материальна, регион тащит вверх гонка "
                "военных потенциалов и взаимное восприятие угрозы.",
    "alliance": "Движущая сила сценария коалиционна, систему расшатывает обвал "
                "доверия к союзным гарантиям и страх стратегического одиночества.",
    "normative": "Движущая сила сценария нормативна, опору подтачивает распад "
                 "институциональных ограничений на применение силы.",
    "mixed": "Движущая сила сценария рассредоточена по нескольким осям сразу, "
             "ни одна не доминирует настолько, чтобы исчерпать картину.",
}

_AXIS_NAME = {
    "material": "материальная гонка потенциалов",
    "alliance": "обвал союзного доверия",
    "normative": "нормативный распад",
}

_REGIME_VERB = {
    "стабильный баланс": "система держится в стабильном балансе",
    "холодная конфронтация": "система пребывает в холодной конфронтации",
    "каскадная дестабилизация": "система входит в каскадную дестабилизацию",
}

_REGIME_FULL = {"S1": "стабильный баланс", "S2": "холодная конфронтация",
                "S3": "каскадная дестабилизация"}


def _full_regime(row: dict) -> str:
    return _REGIME_FULL.get(row.get("regime", row.get("zone")), "")


def _summary(data: dict) -> list:
    """Сводное заключение. Самое важное сверху, как исполнительное резюме."""
    m, v, tt = data["meta"], data["verdict"], data["threat_type"]
    verb = _LEVEL_VERB.get(v.get("level"), "формирует особую конфигурацию риска")
    risk_pct = int(round(v.get("risk", 0) * 100))
    p1 = (f"Сценарий {m['scenario']} на горизонте с {m['base_year']} по "
          f"{m['final_year']} год {verb}. К концу периода вероятность каскадной "
          f"дестабилизации составляет {risk_pct}%, а пиковое напряжение "
          f"достигает величины {data['comparison']['peak']}.")
    p2 = _AXIS_NATURE.get(tt.get("type"), "")
    return [p for p in (p1, p2) if p]


def _known(data: dict) -> list:
    """Исходная обстановка и заданные условия эксперимента."""
    m = data["meta"]
    start = data["timeline"][0]
    paras = [
        f"Отсчёт ведётся от обстановки {m['base_year']} года, когда напряжение "
        f"в регионе оценивается величиной {start['tension']:.3f}, и по совокупности "
        f"показателей {_REGIME_VERB.get(_full_regime(start), 'система сохраняет текущий режим')}. "
        f"Эта точка служит отправной не как предсказание, а как зафиксированный "
        f"исследователем срез настоящего, от которого разворачивается сценарий."
    ]
    # Обоснование отправной точки задокументированными фактами стартового года.
    # Селективно, лишь то, что прямо формирует стартовый уровень напряжения.
    # Якоря ставятся только при наличии всех трёх ключей в реестре, иначе абзац
    # не вставляется, чтобы не родить висячую ссылку.
    by_key = {s["key"]: s for s in data.get("sources", [])}
    base = m["base_year"]
    trio = ("jpn_defense_budget_2026", "jpn_ldp_supermajority_2026",
            "jpn_arms_export_2026")
    if all(k in by_key and by_key[k]["year"] <= base for k in trio):
        paras.append(
            "Эта отправная точка не произвольна. Регион входит в "
            f"{base} год через "
            + _cite("рекордный оборонный бюджет", "jpn_defense_budget_2026")
            + " с опережающим выходом на два процента валового продукта, "
            + _cite("супербольшинство ЛДП", "jpn_ldp_supermajority_2026")
            + " с открыто заявленным курсом на конституционную реформу и "
            + _cite("снятие запрета на экспорт летального оружия", "jpn_arms_export_2026")
            + ". Три события одного года, что вместе двигают японский полюс системы "
            "к более силовому профилю и задают тот уровень напряжения, от которого "
            "стартует сценарий.")
    events = data.get("events", [])
    if events:
        listing = "; ".join(
            f"{ev['year']} год, {ev['description'].rstrip('.')}"
            for ev in events
        )
        paras.append(
            "Сценарий задаёт следующие внешние толчки, наложенные поверх "
            f"инерционной динамики. {listing}. Эти толчки суть не предсказания, "
            "а условия мысленного эксперимента, при которых исследуется отклик "
            "системы.")
    else:
        paras.append(
            "Сценарий не вносит внешних толчков и развивается по одной лишь "
            "внутренней логике, служа инерционным фоном, относительно которого "
            "измеряется тяжесть прочих конфигураций.")
    return paras


def _dynamics(data: dict) -> list:
    """Погодовая динамика напряжения, ход системы по годам."""
    tl = data["timeline"]
    if len(tl) < 2:
        return []
    start, end = tl[0], tl[-1]
    trend = end["tension"] - start["tension"]
    direction = ("нарастающей" if trend > 0.01 else
                 "спадающей" if trend < -0.01 else "стабильной")
    paras = [
        f"На протяжении горизонта напряжение движется по {direction} траектории, "
        f"от {start['tension']:.3f} в {start['year']} году к {end['tension']:.3f} "
        f"в {end['year']}, с пиком {data['comparison']['peak']}. Динамика не "
        f"равномерна, и её переломы сосредоточены в узловых годах."
    ]
    # Переломы по узловым годам, связной прозой.
    breaks = [n for n in data["nodal_years"] if n["kind"] not in ("старт", "финал")]
    if breaks:
        sentences = []
        for n in breaks:
            sentences.append(f"в {n['year']} году {n['kind']}")
        paras.append("Ключевые переломы складываются так. " +
                     "; ".join(sentences).capitalize() + ".")
    return paras


def _drivers_text(data: dict) -> list:
    """Разбор движущих сил по трём осям давления."""
    dr = data["drivers"]
    ranked = dr.get("axis_ranked", [])
    if not ranked:
        return []
    parts = []
    for axis, share in ranked:
        name = _AXIS_NAME.get(axis)
        if name:
            parts.append(f"{name} {int(round(share * 100))}%")
    p1 = ("Раскладка системного давления по трём осям обнаруживает следующее "
          "соотношение вкладов, где " + ", ".join(parts) + ".")
    dominant = _AXIS_NAME.get(ranked[0][0], "доминирующая ось")
    p2 = (f"Преобладает {dominant}, и это означает, что точка возможного "
          f"вмешательства пролегает именно по ней, поскольку снижение давления "
          f"по доминирующей оси уводит систему с текущей траектории эффективнее, "
          f"нежели усилия по второстепенным.")
    paras = [p1, p2]
    if dr.get("japan_mean_erosion") is not None:
        by_key = {s["key"]: s for s in data.get("sources", [])}
        tail = (f"Средняя нормативная эрозия Японии за горизонт держится на уровне "
                f"{dr['japan_mean_erosion']}, что подтверждает её роль того узла, "
                f"через который сценарий передаётся всей подсистеме.")
        # Привязка численного вывода к задокументированной нормативной траектории.
        # Закон 2015 без публичной ссылки рендерится пунктиром, экспорт 2026 ссылкой.
        if "jpn_peace_security_laws_2015" in by_key and "jpn_arms_export_2026" in by_key:
            tail += (" Эта эрозия не абстракция модели, а отражённый числом итог "
                     "задокументированного курса, от "
                     + _cite("законодательного закрепления коллективной самообороны в 2015 году",
                             "jpn_peace_security_laws_2015")
                     + " до "
                     + _cite("снятия экспортных ограничений в 2026",
                             "jpn_arms_export_2026")
                     + ".")
        paras.append(tail)
    return paras


def _comparison_text(data: dict) -> list:
    """Сравнение с инерционным фоном и положение у порога срыва."""
    c = data["comparison"]
    paras = []
    if "excess_final" in c:
        excess = c["excess_final"]
        if excess > 0.005:
            paras.append(
                f"Относительно инерционного дрейфа, где напряжение к концу "
                f"горизонта составляет {c['baseline_final']}, данный сценарий "
                f"добавляет {excess:.3f}. Эта разница и есть мера вклада заданных "
                f"шоков, отделённая от того, что произошло бы и без них.")
        elif excess < -0.005:
            paras.append(
                f"Любопытно, что относительно инерционного дрейфа с финалом "
                f"{c['baseline_final']} данный сценарий оказывается мягче на "
                f"{abs(excess):.3f}, то есть заданные шоки не усугубляют, а "
                f"перенаправляют динамику.")
        else:
            paras.append(
                f"Относительно инерционного дрейфа с финалом {c['baseline_final']} "
                f"данный сценарий почти не отклоняется, что говорит о слабости "
                f"заданных шоков на фоне уже накопленной инерции системы.")
    gap = c.get("gap_to_collapse")
    if gap is not None:
        if gap > 0:
            paras.append(
                f"Пиковое напряжение {c['peak']} отстоит от порога каскадной "
                f"дестабилизации на {gap:.3f}. Для срыва системе недостаёт "
                f"приращения восприятия угрозы или нормативной эрозии, способного "
                f"покрыть этот зазор, и величина зазора есть прямая мера запаса "
                f"прочности региона при данном сценарии.")
        else:
            paras.append(
                f"Пиковое напряжение {c['peak']} пробивает порог каскадной "
                f"дестабилизации, и запас прочности региона при данном сценарии "
                f"исчерпан, система переходит в режим срыва.")
    return paras


def _thought(data: dict) -> list:
    """Результаты моделирования, объединение динамики, драйверов, сравнения."""
    paras = []
    paras += _dynamics(data)
    paras += _drivers_text(data)
    paras += _comparison_text(data)
    # Финальный приговор из последнего среза.
    last = data["timeline"][-1]
    paras.append(
        f"К исходу горизонта напряжение оседает на величине {last['tension']:.3f}, "
        f"и {_REGIME_VERB.get(_full_regime(last), 'система сохраняет режим')}.")
    # Афористическая концовка по уровню.
    level = data["verdict"].get("level")
    if level == "red":
        paras.append("Модель не предрекает войну, она показывает, что при этих "
                     "условиях запас прочности региона исчерпывается, и дальнейшее "
                     "удержание баланса перестаёт быть делом одной лишь сдержанности.")
    elif level == "amber":
        paras.append("Регион не срывается, но и не успокаивается, оставаясь в том "
                     "состоянии настороженного равновесия, которое требует "
                     "постоянного присмотра, а не разовых решений.")
    else:
        paras.append("Регион сохраняет устойчивость, и это та редкая конфигурация, "
                     "где долгосрочная стратегия ещё имеет смысл, а время работает "
                     "не против равновесия.")
    return paras


def _unknown(data: dict) -> list:
    """Пределы достоверности, раздел неопределённости из слоя данных."""
    return list(data.get("gaps", []))


def build_narrative(data: dict) -> dict:
    """
    Главная сборка повествования. Возвращает заголовок и упорядоченные
    разделы для отрисовки на экране и выгрузки в файл из единого источника.
    Каждый раздел суть заголовок и список абзацев.
    """
    m = data["meta"]
    return {
        "title": f"Сценарный отчёт. {m['scenario']}",
        "subtitle": f"Горизонт с {m['base_year']} по {m['final_year']} год, "
                    f"{m['horizon']} шагов",
        "sections": [
            {"heading": "Сводное заключение", "paragraphs": _summary(data)},
            {"heading": "Исходная обстановка", "paragraphs": _known(data)},
            {"heading": "Динамика и результаты моделирования", "paragraphs": _thought(data)},
            {"heading": "Пределы достоверности", "paragraphs": _unknown(data)},
        ],
    }
