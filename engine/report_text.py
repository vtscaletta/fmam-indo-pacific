def _say_safety(s: dict) -> str:
    v = s.get("value")
    if v == "breached":
        return (f"К исходу горизонта система финиширует за порогом срыва, на "
                f"τ = {s['final']} при пороге {s['tau']}. Каскадная дестабилизация "
                f"здесь не прогнозируемый риск, а верифицированный итог.")
    if v == "revived":
        return (f"В экстремуме τ = {s['peak']} система касается порога срыва "
                f"{s['tau']} и отходит к {s['final']}. Каскад инициирован и "
                f"погашен, однако само касание указывает, что устойчивость "
                f"держится на адаптивном отыгрыше, а не на структурном запасе.")
    if v == "edge":
        return (f"Финал τ = {s['final']} локализован у порога срыва {s['tau']}, "
                f"зазор {s['gap']}. В полосе подобной ширины исход детерминируется "
                f"малым приращением восприятия угрозы.")
    if v == "stable":
        return (f"Система финиширует под порогом срыва со структурным запасом, на "
                f"τ = {s['final']} при пороге {s['tau']}. Срыв на горизонте "
                f"моделирования не реализуется.")
    if v == "deep":
        return (f"Система стабилизируется у нижнего порога {s['theta']}, в зоне "
                f"стабильного баланса. Давление диссипирует, а не аккумулируется.")
    return ""


def _say_dominance(d: dict) -> str:
    v = d.get("value")
    if v == "single":
        name = _AXIS_NAME.get(d["axis"], "одна ось")
        return f"Системное давление детерминирует одна ось, {name}, прочие вторичны."
    if v == "duo":
        n1 = _AXIS_NAME.get(d["axes"][0], "первая ось")
        n2 = _AXIS_NAME.get(d["axes"][1], "вторая ось")
        return (f"Давление распределено между двумя осями в равновесии, {n1} и "
                f"{n2}, ни одна не доминирует над другой.")
    if v == "diffuse":
        return "Давление дисперсно по осям без выраженного лидера, профиль рассеян."
    return ""


