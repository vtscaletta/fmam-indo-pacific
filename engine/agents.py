@dataclass(frozen=True)
class AgentState:
    """
    Начальное состояние одного государства-агента на базовый год.

    code      короткий латинский код агента
    name      название на русском
    z1        восприятие угрозы от первичного противника, [0, 1]
    z2        доверие к ключевому союзнику, [0, 1]
    z3        нормативная эрозия ограничений на применение силы, [0, 1]
    milex_bn  военные расходы базового года, млрд долларов 2024
    adversary первичный противник в логике дилеммы безопасности
    note      обоснование позиции с указанием источника
    """
    code: str
    name: str
    z1: float
    z2: float
    z3: float
    milex_bn: float
    adversary: str
    note: str

    def inputs(self) -> tuple[float, float, float]:
        return (self.z1, self.z2, self.z3)


