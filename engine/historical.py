# Опорная точка стыковки: напряжение конца истории и старта прогноза (2026).
FORECAST_ANCHOR_START = 0.560


def validate(simulator: Simulator = None) -> dict:
    """
    Прогоняет ретроспективу и считает метрики валидации. Возвращает отчёт с
    траекторией, набором проверок и общим вердиктом.
    """
    sim = simulator or SIMULATOR
    traj = sim.run(HISTORICAL_SCENARIO, AGENTS_2012, horizon=11, base_year=2012)

    tension = traj.tension
    growth = tension[-1] - tension[0]

    # Год максимального годового прироста напряжения.
    jumps = [tension[i] - tension[i - 1] for i in range(1, len(tension))]
    peak_idx = max(range(len(jumps)), key=lambda i: jumps[i])
    breakpoint_year = traj.years[peak_idx + 1]
    breakpoint_jump = jumps[peak_idx]

    continuity_gap = abs(tension[-1] - FORECAST_ANCHOR_START)

    checks = {
        "growth_positive": growth > 0.08,
        "starts_in_balance": traj.dominant[0] == "S1",
        "settles_in_confrontation": traj.dominant[-1] == "S2",
        "breakpoint_in_2022": breakpoint_year == 2022,
        "continuity_with_forecast": continuity_gap < 0.06,
    }
    verdict = all(checks.values())

    return {
        "trajectory": traj,
        "years": list(traj.years),
        "tension": list(tension),
        "regime": list(traj.dominant),
        "growth": growth,
        "breakpoint_year": breakpoint_year,
        "breakpoint_jump": breakpoint_jump,
        "continuity_gap": continuity_gap,
        "checks": checks,
        "verdict": verdict,
    }
