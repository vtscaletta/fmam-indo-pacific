"""
Матрица межагентного влияния. Вычислительная формализация дилеммы
безопасности на макроуровне связей.

Идея. Стратегическое действие одного агента не остаётся внутри него, а
перетекает во входные переменные других агентов на следующем шаге. Рост
военных расходов государства повышает восприятие угрозы у тех, кто видит в
нём противника. Рост стратегической риторики главного противника понижает
доверие агента к собственному союзнику, актуализируя снайдеровский страх
оставления.

Структура весов. Вес влияния источника i на цель j есть произведение двух
сомножителей. Первый, отношение R, кодирует тип связи: главный противник,
умеренный противник, союзник, отсутствие морского соприкосновения. Союзник
и несоприкасающаяся пара дают ноль, география по Мэхэну отсекает пары без
прямого взаимодействия. Второй, мощь источника, выводится из индекса CINC:
мощное государство пугает сильнее слабого.

Природа чисел. Индекс CINC взят из проекта Correlates of War, набор National
Material Capabilities версии 6.0, покрывающий период до 2016 года. Значения
меняются медленно и служат структурным, замороженным параметром. Тайвань не
входит в систему государств COW и официального CINC не имеет, его значение
дано как авторская прокси-оценка по компонентам индекса и помечено отдельно.
Матрица отношений R operationализирована из открытой стратегической картины
региона. Коэффициент усиления gain калибруется на ретроспективном интервале.
"""

from __future__ import annotations

CODES = ("usa", "chn", "jpn", "twn", "kor")

# Индекс CINC, доля мировой совокупной мощи.
# Источник: Correlates of War, National Material Capabilities v6.0.
# КНР 0.231 по данным 2016 года, прочие порядка 2007-2016 годов.
# Тайвань не входит в COW, значение есть авторская прокси-оценка.
CINC = {
    "usa": 0.137,
    "chn": 0.231,
    "jpn": 0.043,
    "kor": 0.024,
    "twn": 0.006,   # авторская прокси-оценка, не из COW
}

# Матрица отношений R. Ключ источник, вложенный ключ цель, значение
# интенсивность вражды от источника к цели в [0, 1]. Отсутствие ключа
# означает ноль: союзник, либо отсутствие прямого морского соприкосновения
# по Мэхэну. Матрица асимметрична.
#   1.0 экзистенциальный или главный противник
#   0.7 до 0.9 устойчивое соперничество
#   0.4 до 0.6 умеренная настороженность, трения союзников
RELATIONS = {
    "usa": {"chn": 1.0},                                  # КНР главный соперник США
    "chn": {"usa": 0.8, "jpn": 0.9, "twn": 1.0, "kor": 0.5},
    "jpn": {"chn": 0.7, "twn": 0.2, "kor": 0.5},          # ремилитаризация пугает КНР, трения с Кореей
    "twn": {"chn": 0.5},                                  # вооружение Тайваня раздражает КНР
    "kor": {"chn": 0.4, "jpn": 0.4},                      # умеренная настороженность
}

# Старший союзник каждого агента. Через эту связь моделируется доверие.
# США и КНР не имеют старшего союзника в системе прототипа.
ALLY = {
    "jpn": "usa",
    "twn": "usa",   # квазисоюз, без формального договора
    "kor": "usa",
    "usa": None,
    "chn": None,
}


class InfluenceMatrix:
    """
    Матрица влияния пяти агентов и две передаточные функции, переводящие
    действия шага t в приращения входов шага t+1.
    """

    def __init__(self, gain: float = 0.1):
        self.gain = gain
        self.codes = CODES
        self._power = self._power_factors()
        self.W = self._build_weights()

    def _power_factors(self) -> dict:
        """Мощь источника в [0.5, 1.0]. Слабый влияет, но слабее мощного."""
        top = max(CINC.values())
        return {c: 0.5 + 0.5 * (CINC[c] / top) for c in self.codes}

    def _build_weights(self) -> dict:
        """W[i][j] = интенсивность отношения умножить на мощь источника."""
        W = {}
        for i in self.codes:
            W[i] = {}
            for j in self.codes:
                r = RELATIONS.get(i, {}).get(j, 0.0)
                W[i][j] = r * self._power[i]
        return W

    def main_adversary(self, code: str) -> str | None:
        """Источник, сильнее всех повышающий угрозу данного агента."""
        incoming = {i: self.W[i][code] for i in self.codes if i != code}
        if not incoming or max(incoming.values()) == 0.0:
            return None
        return max(incoming, key=incoming.get)

    def threat_delta(self, actions: dict) -> dict:
        """
        Приращение восприятия угрозы каждого агента от военных расходов
        остальных. actions суть словарь код -> вектор действия с ключом milex.
        """
        out = {}
        for j in self.codes:
            s = sum(self.W[i][j] * actions[i]["milex"] for i in self.codes)
            out[j] = s * self.gain
        return out

    def trust_delta(self, actions: dict) -> dict:
        """
        Изменение доверия к союзнику. Рост риторики главного противника
        понижает доверие, отсюда отрицательный знак. Агенты без старшего
        союзника получают ноль.
        """
        out = {}
        for j in self.codes:
            if ALLY.get(j) is None:
                out[j] = 0.0
                continue
            adv = self.main_adversary(j)
            if adv is None:
                out[j] = 0.0
                continue
            out[j] = -self.gain * actions[adv]["rhet"]
        return out


# Готовый экземпляр со стандартным усилением.
INFLUENCE = InfluenceMatrix()
