"""
Марковское ядро макроуровня. Управляемая цепь трёх фазовых режимов
регионального комплекса безопасности.

Режимы.
    S1 устойчивый баланс
    S2 холодная конфронтация
    S3 каскадная дестабилизация

Управляемость. Матрица переходов не постоянна. На каждом шаге она
пересчитывается как функция системного напряжения, агрегированного из
совокупной стратегической активности агентов микроуровня. Напряжение
приходит извне, ядро принимает его числом в [0, 1].

Конструкция вероятности перехода. Вероятность из режима i в режим j есть
произведение двух множителей. Привлекательность целевого режима при данном
напряжении: баланс притягивает при низком напряжении, конфронтация при
среднем, дестабилизация при высоком. Близость режимов штрафует дальние
прыжки, отчего система не срывается из баланса прямо в коллапс, а ползёт
через промежуточный режим. Отсюда каскадность переходов, инерция текущего
режима и гистерезис: при равном напряжении свалиться и восстановиться
одинаково трудно.

Начальное распределение помечено как предварительное и отражает тезис о
нахождении региона в фазовом переходе от однополярной конфигурации к
оспариваемой многополярности при сохраняющемся преобладании баланса.
"""

from __future__ import annotations
import numpy as np


REGIMES = ("S1", "S2", "S3")
REGIME_NAMES = {
    "S1": "устойчивый баланс",
    "S2": "холодная конфронтация",
    "S3": "каскадная дестабилизация",
}

# Предварительное начальное распределение по режимам на базовый год.
INITIAL_DISTRIBUTION = np.array([0.55, 0.35, 0.10])


class MarkovCore:
    """
    Управляемая марковская цепь трёх режимов. Строит матрицу переходов от
    напряжения и продвигает распределение по режимам на шаг вперёд.
    """

    def __init__(self, proximity_decay: float = 0.2, floor: float = 0.05):
        # proximity_decay: чем меньше, тем труднее дальний прыжок через режим.
        # floor: базовая привлекательность, удерживает вероятности от нуля.
        self.proximity_decay = proximity_decay
        self.floor = floor
        self.n = len(REGIMES)

    def _attractiveness(self, tension: float) -> np.ndarray:
        """Привлекательность каждого режима при данном напряжении."""
        a1 = 1.0 - tension          # баланс, тяга при низком напряжении
        a2 = 1.0 - abs(2.0 * tension - 1.0)  # конфронтация, пик при среднем
        a3 = tension                # дестабилизация, тяга при высоком
        return np.array([a1, a2, a3]) + self.floor

    def transition_matrix(self, tension: float) -> np.ndarray:
        """Матрица переходов P(t) размерности три на три при данном напряжении."""
        if not (0.0 <= tension <= 1.0):
            raise ValueError(f"напряжение вне диапазона [0, 1]: {tension}")
        a = self._attractiveness(tension)
        P = np.zeros((self.n, self.n))
        for i in range(self.n):
            for j in range(self.n):
                P[i, j] = a[j] * (self.proximity_decay ** abs(i - j))
            P[i] /= P[i].sum()
        return P

    def step(self, distribution: np.ndarray, tension: float) -> np.ndarray:
        """Продвигает распределение по режимам на один шаг."""
        dist = np.asarray(distribution, dtype=float)
        if abs(dist.sum() - 1.0) > 1e-6:
            raise ValueError(f"распределение не нормировано: сумма {dist.sum()}")
        return dist @ self.transition_matrix(tension)

    @staticmethod
    def dominant_regime(distribution: np.ndarray) -> str:
        """Код наиболее вероятного режима для считывания состояния системы."""
        return REGIMES[int(np.argmax(distribution))]


# Готовое ядро со стандартными параметрами.
MARKOV = MarkovCore()
