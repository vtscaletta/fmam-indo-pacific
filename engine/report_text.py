def _summary(data: dict) -> list:
    """Сводное заключение. Самое важное сверху, как исполнительное резюме."""
    m, v, tt = data["meta"], data["verdict"], data["threat_type"]
    verb = _LEVEL_VERB.get(v.get("level"), "формирует особую конфигурацию риска")
    risk_pct = int(round(v.get("risk", 0) * 100))
    p1 = (f"Сценарная симуляция {m['scenario']} на временном горизонте "
          f"{m['base_year']}\u2013{m['final_year']} гг. {verb}. К финальной точке "
          f"прогноза вероятность каскадной дестабилизации стабилизируется на "
          f"отметке {risk_pct}% при экстремальном значении системного напряжения "
          f"{data['comparison']['peak']}.")
    p2 = _AXIS_NATURE.get(tt.get("type"), "")
    return [p for p in (p1, p2) if p]


def _known(data: dict) -> list:
    """Исходная обстановка. Срез базового года и экзогенные шоки, вплетённые
    связной прозой с привязкой к дёрнутой переменной z, как требует эталон.
    Без действующих лиц, протагонисты проявляются лишь в ходе прогона."""
    m = data["meta"]
    start = data["timeline"][0]
    regime = _REGIME_VERB.get(_full_regime(start), "система сохраняет текущий режим")
    paras = [
        f"Стартовая конфигурация параметров (базовый {m['base_year']} год) "
        f"оценивает совокупное системное напряжение величиной {start['tension']:.3f}, "
        f"и по совокупности параметров {regime}."
    ]
    events = data.get("events", [])
    if events:
        zsym = {"z1": "z\u2081", "z2": "z\u2082", "z3": "z\u2083"}
        items = []
        for ev in events:
            z = zsym.get(ev.get("variable"))
            tag = f" ({z})" if z else ""
            items.append(f"{ev['year']} год \u2014 {ev['description'].rstrip('.')}{tag}")
        listing = "; ".join(items)
        paras.append(
            "Поверх инерционной динамики модель последовательно вводит комплекс "
            f"взаимосвязанных экзогенных шоков. {listing}.")
    else:
        paras.append(
            "Сценарий не вводит экзогенных шоков и развивается по внутренней "
            "логике, выступая инерционным фоном, относительно которого измеряется "
            "дестабилизирующий эффект прочих конфигураций.")
    return paras


def _actor_calibration(a: dict, data: dict, axis: str) -> str:
    """Конфигурация исходных параметров одного агента. Три стартовые переменные
    даются с индексами теоретической главы, восприятие угрозы z\u2081, доверие к
    союзнику z\u2082, нормативная эрозия z\u2083, чтобы за цифрой читался параметр движка, а
    не вкусовая шкала. Эмпирическое закрепление вплетается без мета-языка
    цитирования, цветная ссылка ведёт к источнику сама."""
    z = a.get("start_z")
    name = a["name"]
    if z and len(z) == 3:
        lead = (f"{name} входит в сценарий с конфигурацией параметров, "
                f"восприятие угрозы (z\u2081 = {z[0]:.2f}), доверие к союзнику "
                f"(z\u2082 = {z[1]:.2f}), нормативная эрозия (z\u2083 = {z[2]:.2f}).")
    else:
        lead = f"{name} входит в сценарий."
    anchors = _start_anchors(a["code"], data, axis)
    if anchors:
        return lead + f" Эмпирически данная конфигурация отражает {_join_ru(anchors)}."
    return lead


def _actors_prose(data: dict) -> str:
    """
    Ведущие акторы прогона для раздела динамики. Имена в именительном как
    подлежащие, чтобы не склонять и не плодить ложную причинность, за именем
    конфигурация исходных параметров. Реагенты отсеяны ещё в данных.
    """
    protag = [a for a in data.get("actors", []) if a.get("is_protagonist")][:2]
    if not protag:
        return ""
    names = [a["name"] for a in protag]
    dominant_axis = data.get("drivers", {}).get("dominant_axis")
    if len(protag) == 2:
        head = (f"Системную траекторию сценария задают {names[0]} и {names[1]}, "
                f"их конфигурация определяет поведение подсистемы в целом.")
    else:
        head = f"Системную траекторию сценария задаёт {names[0]}."
    parts = [head] + [_actor_calibration(a, data, dominant_axis) for a in protag]
    return " ".join(parts)


def _dynamics(data: dict) -> list:
    """Погодовая динамика системного напряжения в академическом реализме."""
    tl = data["timeline"]
    if len(tl) < 2:
        return []
    paras = []
    lead = _actors_prose(data)
    if lead:
        paras.append(lead)
    start, end = tl[0], tl[-1]
    trend = end["tension"] - start["tension"]
    monotone = all(tl[i]["tension"] >= tl[i - 1]["tension"] - 1e-9
                   for i in range(1, len(tl)))
    if trend > 0.01:
        direction = "кумулятивный рост"
        coda = (", без фаз адаптивной релаксации" if monotone else
                ", с локальными колебаниями")
    elif trend < -0.01:
        direction = "кумулятивный спад"
        coda = ""
    else:
        direction = "стационарную динамику"
        coda = ""
    paras.append(
        f"Горизонт моделирования фиксирует {direction} системного напряжения, "
        f"от {start['tension']:.3f} в начальной точке {start['year']} года до "
        f"{end['tension']:.3f} к финалу {end['year']}{coda}, с экстремумом "
        f"{data['comparison']['peak']}. Динамика неравномерна, и её структурные "
        f"переломы локализованы в узловых годах.")
    breaks = [n for n in data["nodal_years"] if n["kind"] not in ("старт", "финал")]
    if breaks:
        sentences = [f"в {n['year']} году {n['kind']}" for n in breaks]
        paras.append("Локализация переломов такова, " +
                     "; ".join(sentences) + ".")
    return paras


def _drivers_text(data: dict) -> list:
    """Декомпозиция системного давления по трём осям."""
    dr = data["drivers"]
    ranked = dr.get("axis_ranked", [])
    if not ranked:
        return []
    parts = []
    for axis, share in ranked:
        name = _AXIS_NAME.get(axis)
        if name:
            parts.append(f"{name} {int(round(share * 100))}%")
    p1 = ("Декомпозиция системного давления по трём осям фиксирует распределение "
          "вкладов, " + ", ".join(parts) + ".")
    return [p1]


def _comparison_text(data: dict) -> list:
    """Изоляция вклада сценарных шоков и позиция относительно порога срыва."""
    c = data["comparison"]
    paras = []
    if "excess_final" in c:
        excess = c["excess_final"]
        if excess > 0.005:
            paras.append(
                f"Изоляция сценарных шоков относительно инерционного тренда, "
                f"финиширующего на τ = {c['baseline_final']}, фиксирует их чистый "
                f"вклад, плюс {excess:.3f} пункта. Данный зазор есть мера "
                f"дестабилизирующего эффекта триггера, отделённая от фоновой "
                f"динамики, а не сумма наблюдаемого и унаследованного.")
        elif excess < -0.005:
            paras.append(
                f"Относительно инерционного тренда с финалом τ = {c['baseline_final']} "
                f"сценарий оказывается мягче на {abs(excess):.3f}, что указывает на "
                f"перенаправляющий, а не усугубляющий характер заданных шоков.")
        else:
            paras.append(
                f"Относительно инерционного тренда с финалом τ = {c['baseline_final']} "
                f"отклонение пренебрежимо, что свидетельствует о доминировании уже "
                f"накопленной системной инерции над эффектом сценарных шоков.")
    return paras


