def _cite(phrase: str, key: str) -> str:
    """Оборачивает фразу прозы якорем источника. Слой представления превратит
    якорь в инлайн-ссылку, потому текстовый слой остаётся свободен от HTML.
    Маркер [[ключ||фраза]] в естественной прозе не встречается, потому безопасен
    при экранировании и парсинге. Селективность за вызывающим, якорь ставится
    лишь там, где утверждение прямо опирается на задокументированный факт."""
    return f"[[{key}||{phrase}]]"


