def _say_safety(s: dict) -> str:
    v = s.get("value")
    if v == "breached":
        return (f"К исходу горизонта система финиширует за порогом срыва, на "
                f"величине {s['final']} при пороге {s['tau']}. Каскадная "
                f"дестабилизация здесь не угроза впереди, а уже состоявшийся итог.")
    if v == "revived":
        return (f"В пике {s['peak']} система касается порога срыва {s['tau']} и "
                f"отходит к {s['final']}. Каскад был запущен и погашен, но само "
                f"касание говорит, что устойчивость держится на отыгрыше, не на запасе.")
    if v == "edge":
        return (f"Финал {s['final']} прижат к порогу срыва {s['tau']}, зазор всего "
                f"{s['gap']}. В столь узкой полосе исход решает малое приращение "
                f"восприятия угрозы.")
    if v == "stable":
        return (f"Система финиширует под порогом срыва с запасом, на {s['final']} "
                f"при пороге {s['tau']}. Срыв горизонту не грозит.")
    if v == "deep":
        return (f"Система оседает у нижнего порога {s['theta']}, в зоне стабильного "
                f"баланса. Давление рассасывается, а не накапливается.")
    return ""


def _say_dominance(d: dict) -> str:
    v = d.get("value")
    if v == "single":
        name = _AXIS_NAME.get(d["axis"], "одна ось")
        return f"Давление держит одна ось, {name}, прочие вторичны."
    if v == "duo":
        n1 = _AXIS_NAME.get(d["axes"][0], "первая ось")
        n2 = _AXIS_NAME.get(d["axes"][1], "вторая ось")
        return f"Давление несут две оси в клинче, {n1} и {n2}, и ни одна не уступает."
    if v == "diffuse":
        return "Давление размазано по осям без явного лидера, профиль рассеян."
    return ""


def _say_shape(sh: dict) -> str:
    v = sh.get("value")
    if v == "sharp":
        return ("Движение резкое, почти весь сдвиг забирает один год, и значит "
                "исход решается в коротком окне, а не вызревает постепенно.")
    if v == "creeping":
        return ("Движение ползучее, сдвиг растянут по горизонту, и исход не "
                "точечный, а накопительный.")
    if v == "oscillating":
        return "Траектория колеблется вверх и вниз без устойчивого направления."
    return ""


def _say_tempo(t: dict) -> str:
    v = t.get("value")
    if v == "explosive":
        return (f"Пик приходит рано, в {t['year']} году, дальше система живёт с уже "
                f"состоявшимся обострением.")
    if v == "linear":
        return f"Пик вызревает к середине горизонта, к {t['year']} году."
    if v == "delayed":
        return (f"Пик отложен к концу, на {t['year']} год, обострение зреет медленно, "
                f"как поздняя мина.")
    return ""


def _say_shift(sf: dict) -> str:
    v = sf.get("value")
    if v == "handover":
        f_ = _AXIS_GEN.get(sf["from_axis"], "одной оси")
        t_ = _AXIS_DAT.get(sf["to_axis"], "другой")
        return (f"По ходу горизонта верх берёт другая ось, давление переходит от "
                f"{f_} к {t_}, и к финалу система болеет не тем, чем заболела вначале.")
    if v == "swing":
        if sf.get("normative_anomaly"):
            return ("Доминанта мечется, и нормативная ось уходит и возвращается. По "
                    "механике модели нормативный откат почти исключён, асимметричное "
                    "забывание дрейфа работает как храповик, потому качель здесь "
                    "сигналит о пограничном прогоне, а не о рядовой динамике.")
        return "Доминанта мечется между осями, устойчивого профиля давления нет."
    return ""


def _say_actors(a: dict) -> str:
    v = a.get("value")
    if v == "hegemon":
        return f"Сцену держит один актёр, {a['leader']}, остальные уходят в фон."
    if v == "melee":
        return ("Давление многополярно, за парой лидеров стоит плотная группа почти "
                "равных по весу, и сдерживать приходится не одного противника.")
    return ""


def _say_counterfactual(c: dict) -> str:
    """Суждение о вкладе сценария сверх инерции. Виноват ли сам сценарий в том,
    куда пришла система, или она пришла бы туда и без него."""
    v = c.get("value")
    if v == "aggravating":
        return ("Вина за подъём лежит на самом сценарии, против инерции он гонит "
                "напряжение вверх, а не плывёт по течению.")
    if v == "mitigating":
        return ("Парадокс прогона в том, что сценарий работает против срыва, инерция "
                "вела к каскаду, а заданные толчки систему удержали.")
    if v == "inertial" and c.get("soft"):
        return ("Сценарий чуть смягчает фон, но от срыва не спасает, и видимое "
                "облегчение обманчиво.")
    if v == "inertial":
        return ("Сценарий почти не отклоняет систему от инерции, его толчки тонут "
                "в уже накопленном дрейфе.")
    return ""


def _say_shape_tempo(sh: dict, t: dict) -> str:
    """Форма и темп одной увязанной фразой, чтобы резкое и медленно зреющее не
    стояли рядом как противоречие. Резкий поздний скачок это не парадокс, а
    тихое накопление с прорывом под конец."""
    shv, tv, yr = sh.get("value"), t.get("value"), t.get("year")
    if shv == "sharp":
        if tv == "delayed":
            return (f"Почти весь сдвиг забирает один год, и приходит он поздно, "
                    f"к {yr}, обострение копится тихо и прорывается рывком под конец.")
        if tv == "explosive":
            return (f"Удар резкий и ранний, к {yr} году, дальше система живёт с уже "
                    f"состоявшимся обострением.")
        return (f"Сдвиг резкий и ложится к середине горизонта, к {yr} году, исход "
                f"решается в коротком окне, а не вызревает постепенно.")
    if shv == "creeping":
        when = {"explosive": f"ранним пиком к {yr}", "linear": f"пиком к середине, {yr}",
                "delayed": f"пиком под конец, к {yr}"}.get(tv, "")
        tail = f", с {when}" if when else ""
        return (f"Движение ползучее, сдвиг растянут по горизонту{tail}, и исход "
                f"накопительный, а не точечный.")
    if shv == "oscillating":
        return "Траектория колеблется вверх и вниз без устойчивого направления."
    return ""


def _say_prescription(s: dict, d: dict) -> str:
    """
    Управленческое предписание. Сопоставляет уязвимость с доминантой и называет
    рычаг удержания. При двухфакторном давлении нормативной и материальной осей у
    порога вводит антирычаг, балансировать материальным здесь значит детонировать
    каскад. Предохранитель резкости сохранён, у кромки тон сдержан, рычаг назван
    без бахвальства уверенностью, которой в тонком зазоре нет.
    """
    sv = s.get("value")
    val = d.get("value")
    axis = d.get("axis") if val == "single" else (
        d.get("axes", [None])[0] if val == "duo" else None)
    lever = _AXIS_LEVER.get(axis)
    # Антирычаг. Двухфакторное норм плюс мат у порога, материальный путь губителен.
    if sv in ("breached", "revived", "edge") and val == "duo":
        if {"normative", "material"} <= set(d.get("axes", [])):
            core = ("Балансировать через гонку потенциалов здесь значит подтолкнуть "
                    "детонацию, материальная ось уже на пределе. Единственный рычаг "
                    "удержания лежит на искусственной заморозке нормативной оси, "
                    "возврате правовых ограничений на применение силы.")
            if sv == "edge":
                return core + " Зазор тонок, и цена промаха в выборе рычага высока."
            return core
    if sv in ("breached", "revived"):
        if lever:
            return (f"Рычаг удержания лежит {lever}, давление по ней уводит систему "
                    f"с траектории срыва вернее усилий по второстепенным. Удержание "
                    f"баланса перестаёт быть делом одной сдержанности.")
        return ("Систему тянет за порог по совокупности осей сразу, и удержание "
                "баланса перестаёт быть делом одной сдержанности.")
    if sv == "edge":
        tail = ("и вблизи порога вывод держится на тонком, где резкость суждения "
                "была бы самонадеянностью.")
        if lever:
            return f"Рычаг удержания лежит {lever}, {tail}"
        return f"Точка приложения силы размыта между осями, {tail}"
    if sv in ("stable", "deep"):
        return ("Конфигурация оставляет пространство для долгой игры, удержание не "
                "требует экстренных мер и допускает расчёт на годы вперёд.")
    return ""


def _judgment_prose(data: dict) -> list:
    """
    Собирает суждение по решётке в связную прозу двумя блоками. Блок ядра отвечает
    по порядку важности, погибнет ли система, кто её давит, виновен ли сценарий.
    Блок механики описывает, как именно система шла к финалу, форму с темпом,
    смену природы давления и расстановку. Завершает управленческое предписание.
    Молчит честно там, где сценарий не родил динамики.
    """
    j = data.get("judgment", {})
    if not j:
        return []
    # Честное молчание. Никто никого не двигает и сценарий идёт по инерции.
    if (j.get("actors", {}).get("value") == "lull"
            and j.get("counterfactual", {}).get("value") == "background"):
        return ["Сценарий не порождает собственной динамики, система идёт по "
                "инерции, и судить сверх самого фона не о чем."]
    paras = []
    # Блок ядра суждения.
    block1 = " ".join(p for p in (_say_safety(j.get("safety", {})),
                                  _say_dominance(j.get("dominance", {})),
                                  _say_counterfactual(j.get("counterfactual", {}))) if p)
    if block1:
        paras.append(block1)
    # Блок механики прогона.
    block2 = " ".join(p for p in (_say_shape_tempo(j.get("shape", {}), j.get("tempo", {})),
                                  _say_shift(j.get("shift", {})),
                                  _say_actors(j.get("actors", {}))) if p)
    if block2:
        paras.append(block2)
    # Управленческое предписание.
    presc = _say_prescription(j.get("safety", {}), j.get("dominance", {}))
    if presc:
        paras.append(presc)
    return paras


def _thought(data: dict) -> list:
    """Результаты моделирования. Факты динамики, драйверов и контрфакта, поверх
    которых ложится суждение по решётке."""
    paras = []
    paras += _dynamics(data)
    paras += _drivers_text(data)
    paras += _comparison_text(data)
    # Суждение по решётке вместо шаблонной концовки по уровню риска. Вывод
    # выводится из самого прогона, а резкость тона привязана к близости к порогу.
    paras += _judgment_prose(data)
    return paras


def _unknown(data: dict) -> list:
    """Пределы достоверности, раздел неопределённости из слоя данных."""
    return list(data.get("gaps", []))


