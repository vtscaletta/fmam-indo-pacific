def _summary(data: dict) -> list:
    """Сводное заключение. Самое важное сверху, как исполнительное резюме."""
    m, v, tt = data["meta"], data["verdict"], data["threat_type"]
    verb = _LEVEL_VERB.get(v.get("level"), "формирует особую конфигурацию риска")
    risk_pct = int(round(v.get("risk", 0) * 100))
    p1 = (f"Сценарий {m['scenario']} на горизонте с {m['base_year']} по "
          f"{m['final_year']} год {verb}. К концу периода вероятность каскадной "
          f"дестабилизации составляет {risk_pct}%, а пиковое напряжение "
          f"достигает величины {data['comparison']['peak']}.")
    p2 = _AXIS_NATURE.get(tt.get("type"), "")
    return [p for p in (p1, p2) if p]


def _known(data: dict) -> list:
    """Исходная обстановка. Чистый срез базового года и заданные толчки. Без
    действующих лиц, ибо на старте сценарий ещё не разошёлся с инерцией, а
    протагонисты проявляются лишь в ходе прогона, не в отправной точке."""
    m = data["meta"]
    start = data["timeline"][0]
    paras = [
        f"Отсчёт ведётся от обстановки {m['base_year']} года, когда напряжение "
        f"в регионе оценивается величиной {start['tension']:.3f}, и по совокупности "
        f"показателей {_REGIME_VERB.get(_full_regime(start), 'система сохраняет текущий режим')}."
    ]
    events = data.get("events", [])
    if events:
        listing = "; ".join(
            f"{ev['year']} год, {ev['description'].rstrip('.')}"
            for ev in events
        )
        paras.append(
            "Сценарий задаёт следующие внешние толчки, наложенные поверх "
            f"инерционной динамики. {listing}.")
    else:
        paras.append(
            "Сценарий не вносит внешних толчков и развивается по одной лишь "
            "внутренней логике, служа инерционным фоном, относительно которого "
            "измеряется тяжесть прочих конфигураций.")
    return paras


def _actors_prose(data: dict) -> str:
    """
    Действующие лица прогона для раздела динамики. Здесь, в ходе сценария, а не
    в стартовом срезе, корректно назвать тех, кто его вёл. Имена даются в
    именительном через связку, чтобы не склонять их в косвенные падежи. Реагенты
    сюда не попадают, отбор отсеял их ещё в данных.
    """
    protag = [a for a in data.get("actors", []) if a.get("is_protagonist")][:2]
    if not protag:
        return ""
    names = [a["name"] for a in protag]
    dominant_axis = data.get("drivers", {}).get("dominant_axis")
    if len(protag) == 2:
        head = (f"Ход сценария ведут {names[0]} и {names[1]}, и от их положения "
                f"разворачивается остальное.")
        segs = []
        for ref, a in zip(("первого", "второго"), protag):
            anchors = _start_anchors(a["code"], data, dominant_axis)
            segs.append(f"{ref} опираются на {_join_ru(anchors)}" if anchors
                        else f"{ref} пока не находят документального якоря в реестре")
        return head + " Исходные показатели " + ", ".join(segs) + "."
    head = f"Ход сценария ведёт {names[0]}."
    anchors = _start_anchors(protag[0]["code"], data, dominant_axis)
    return (head + f" Его исходные показатели опираются на {_join_ru(anchors)}."
            if anchors else
            head + " Его исходные показатели пока не находят документального "
            "якоря в реестре.")


def _dynamics(data: dict) -> list:
    """Погодовая динамика напряжения, ход системы по годам."""
    tl = data["timeline"]
    if len(tl) < 2:
        return []
    paras = []
    lead = _actors_prose(data)
    if lead:
        paras.append(lead)
    start, end = tl[0], tl[-1]
    trend = end["tension"] - start["tension"]
    direction = ("нарастающей" if trend > 0.01 else
                 "спадающей" if trend < -0.01 else "стабильной")
    paras.append(
        f"На протяжении горизонта напряжение движется по {direction} траектории, "
        f"от {start['tension']:.3f} в {start['year']} году к {end['tension']:.3f} "
        f"в {end['year']}, с пиком {data['comparison']['peak']}. Динамика не "
        f"равномерна, и её переломы сосредоточены в узловых годах.")
    # Переломы по узловым годам, связной прозой.
    breaks = [n for n in data["nodal_years"] if n["kind"] not in ("старт", "финал")]
    if breaks:
        sentences = []
        for n in breaks:
            sentences.append(f"в {n['year']} году {n['kind']}")
        paras.append("Ключевые переломы складываются так. " +
                     "; ".join(sentences).capitalize() + ".")
    return paras


def _drivers_text(data: dict) -> list:
    """Разбор движущих сил по трём осям давления."""
    dr = data["drivers"]
    ranked = dr.get("axis_ranked", [])
    if not ranked:
        return []
    parts = []
    for axis, share in ranked:
        name = _AXIS_NAME.get(axis)
        if name:
            parts.append(f"{name} {int(round(share * 100))}%")
    p1 = ("Раскладка системного давления по трём осям обнаруживает следующее "
          "соотношение вкладов, где " + ", ".join(parts) + ".")
    return [p1]


def _comparison_text(data: dict) -> list:
    """Сравнение с инерционным фоном и положение у порога срыва."""
    c = data["comparison"]
    paras = []
    if "excess_final" in c:
        excess = c["excess_final"]
        if excess > 0.005:
            paras.append(
                f"Относительно инерционного дрейфа, где напряжение к концу "
                f"горизонта составляет {c['baseline_final']}, данный сценарий "
                f"добавляет {excess:.3f}. Данная разница и есть мера вклада заданных "
                f"шоков, отделённая от того, что произошло бы и без них.")
        elif excess < -0.005:
            paras.append(
                f"Любопытно, что относительно инерционного дрейфа с финалом "
                f"{c['baseline_final']} данный сценарий оказывается мягче на "
                f"{abs(excess):.3f}, то есть заданные шоки не усугубляют, а "
                f"перенаправляют динамику.")
        else:
            paras.append(
                f"Относительно инерционного дрейфа с финалом {c['baseline_final']} "
                f"данный сценарий почти не отклоняется, что говорит о слабости "
                f"заданных шоков на фоне уже накопленной инерции системы.")
    gap = c.get("gap_to_collapse")
    if gap is not None:
        if gap > 0:
            paras.append(
                f"Пиковое напряжение {c['peak']} отстоит от порога каскадной "
                f"дестабилизации на {gap:.3f}. Для срыва системе недостаёт "
                f"приращения восприятия угрозы или нормативной эрозии, способного "
                f"покрыть данный зазор, и величина зазора есть прямая мера запаса "
                f"прочности региона при данном сценарии.")
        else:
            paras.append(
                f"Пиковое напряжение {c['peak']} пробивает порог каскадной "
                f"дестабилизации, и запас прочности региона при данном сценарии "
                f"исчерпан, система переходит в режим срыва.")
    return paras


