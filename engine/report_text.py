"""
Слой текста отчёта. Модуль 12, повествование.

Превращает структуру данных отчёта в аналитическую прозу академического
строя. Разделы следуют разведывательному стандарту, исходная обстановка,
результаты моделирования, пределы достоверности, но поданы строгим научным
тоном, без разговорных ярлыков. Текст собирается детерминированно из фактов
прогона, потому отчёт воспроизводим и при повторной сборке тождествен себе.

Разделение ответственности. Модуль не считает и не извлекает данные, только
облекает готовую структуру в слова. Числа берёт из build_report_data как
есть. Стиль выдержан без двоеточий и длинных тире, нарративной прозой,
оценочные суждения прямые.
"""

from __future__ import annotations


def _cite(phrase: str, key: str) -> str:
    """Оборачивает фразу прозы якорем источника. Слой представления превратит
    якорь в инлайн-ссылку, потому текстовый слой остаётся свободен от HTML.
    Маркер [[ключ||фраза]] в естественной прозе не встречается, потому безопасен
    при экранировании и парсинге. Селективность за вызывающим, якорь ставится
    лишь там, где утверждение прямо опирается на задокументированный факт."""
    return f"[[{key}||{phrase}]]"


# Короткие анкорные фразы стартовых позиций в винительном падеже, по ключу
# источника. Рамка обоснования у нас единая, опирается на что-то, потому падеж
# фиксирован и имя агента в косвенный падеж не попадает. По мере наполнения
# реестра прочими агентами в M-C сюда добавляются их фразы тем же образцом.
_START_ANCHOR = {
    "chn_senkaku_presence_2024": "рекордное присутствие у островов Сэнкаку",
    "jpn_defense_budget_2026": "рекордный оборонный бюджет 2026 года",
    "jpn_ldp_supermajority_2026": "супербольшинство ЛДП с курсом на конституционную реформу",
    "jpn_arms_export_2026": "снятие запрета на экспорт летального оружия",
    "jpn_nss_counterstrike_2022": "доктрину потенциала ответного удара",
    "jpn_takaichi_taiwan_2025": "сигнал о вовлечении при кризисе вокруг Тайваня",
    "jpn_collective_defense_2014": "толкование о праве на коллективную самооборону",
    "jpn_peace_security_laws_2015": "законы о мире и безопасности 2015 года",
    "jpn_senkaku_nationalization_2012": "национализацию островов Сэнкаку",
}


def _join_ru(items: list) -> str:
    """Перечисление по-русски, a, b и c."""
    if not items:
        return ""
    if len(items) == 1:
        return items[0]
    return ", ".join(items[:-1]) + " и " + items[-1]


def _start_anchors(code: str, data: dict, dominant_axis: str) -> list:
    """
    Якоря стартовой позиции одного агента, отобранные сценарно. Берутся факты
    реестра, помеченные этим агентом, приоритет фактам доминирующей оси, свежие
    важнее, дозировано до двух. Если фразы для ключа нет в словаре, ключ
    пропускается, сырой факт в прозу не лезет.
    """
    srcs = [s for s in data.get("sources", []) if s.get("agent") == code]
    on_axis = [s for s in srcs if s.get("axis") == dominant_axis]
    pool = on_axis if on_axis else srcs
    pool = sorted(pool, key=lambda s: s["year"], reverse=True)
    out = []
    for s in pool:
        phrase = _START_ANCHOR.get(s["key"])
        if phrase:
            out.append(_cite(phrase, s["key"]))
        if len(out) >= 2:
            break
    return out


